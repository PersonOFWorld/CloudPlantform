package com.cloud.entity;

public class CounterBean {

	private String personalMachineNum;
	private String personalMachinePwd;
	private String systemType;
	

	public String getPersonalMachineNum() {
		return personalMachineNum;
	}
	public void setPersonalMachineNum(String personalMachineNum) {
		this.personalMachineNum = personalMachineNum;
	}
	public String getPersonalMachinePwd() {
		return personalMachinePwd;
	}
	public void setPersonalMachinePwd(String personalMachinePwd) {
		this.personalMachinePwd = personalMachinePwd;
	}
	public String getSystemType() {
		return systemType;
	}
	public void setSystemType(String machineType) {
		this.systemType = machineType;
	}
	
}
